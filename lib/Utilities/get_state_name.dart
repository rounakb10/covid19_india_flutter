String getStateNameFromCode(String code) {
  switch (code) {
    case 'AN':
      return 'Andaman and Nicobar Islands';
    case 'AP':
      return 'Andhra Pradesh';
    case 'AR':
      return 'Arunachal Pradesh';
    case 'AS':
      return 'Assam';
    case 'BR':
      return 'Bihar';
    case 'CH':
      return 'Chandigarh';
    case 'CT':
      return 'Chhattisgarh';
    case 'DN':
      return 'Dadra and Nagar Haveli and Daman and Diu';
    case 'DL':
      return 'Delhi';
    case 'GA':
      return 'Goa';
    case 'GJ':
      return 'Gujarat';
    case 'HR':
      return 'Haryana';
    case 'HP':
      return 'Himachal Pradesh';
    case 'JK':
      return 'Jammu and Kashmir';
    case 'JH':
      return 'Jharkhand';
    case 'KA':
      return 'Karnataka';
    case 'KL':
      return 'Kerala';
    case 'LA':
      return 'Ladakh'; //Ladakh
    case 'MP':
      return 'Madhya Pradesh';
    case 'MH':
      return 'Maharashtra';
    case 'MN':
      return 'Manipur';
    case 'ML':
      return 'Meghalaya';
    case 'MZ':
      return 'Mizoram';
    case 'NL':
      return 'Nagaland';
    case 'OR':
      return 'Nagaland';
    case 'PY':
      return 'Puducherry';
    case 'PB':
      return 'Punjab';
    case 'RJ':
      return 'Rajasthan';
    case 'SK':
      return 'Sikkim';
    case 'TN':
      return 'Tamil Nadu';
    case 'TG':
      return 'Telangana';
    case 'TR':
      return 'Tripura';
    case 'UT':
      return 'Uttarakhand';
    case 'UP':
      return 'Uttar Pradesh';
    case 'WB':
      return 'West Bengal';
  }
  return '';
}
